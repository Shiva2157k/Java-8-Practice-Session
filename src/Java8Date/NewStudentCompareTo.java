package Java8Date;

public class NewStudentCompareTo {

    private int id;
    private String fName;
    private double cGpa;

    public int getId() {
        return id;
    }
    public String getfName(){
        return fName;
    }
    public double getcGpa(){
        return cGpa;
    }

    public NewStudentCompareTo(int id, String fName,double cGpa){
        super();
        this.id = id;
        this.fName = fName;
        this.cGpa = cGpa;
    }
}
